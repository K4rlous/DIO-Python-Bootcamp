# Tipos de dados em Python
print(1 + 10) # Soma de inteiros
print(1.5 + 10.5) # Soma de floats
print(True) # Booleano
print("Python") # String
print([1, 2, 3]) # Lista
print((1, 2, 3)) # Tupla
print({1, 2, 3}) # Conjunto
print({"nome": "João", "idade": 30}) # Dicionário

# Por debaixo dos panos o Python utiliza classes para representar os tipos de dados
int(10) # Classe int
float(10.5) # Classe float
bool(True) # Classe bool
str("Python") # Classe str
list([1, 2, 3]) # Classe list
tuple((1, 2, 3)) # Classe tuple
set({1, 2, 3}) # Classe set
dict({"nome": "João", "idade": 30}) # Classe dict
