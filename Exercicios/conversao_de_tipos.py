# Inteiro para float
preco = 10
print(preco) # valor: 10

preco = float(preco)
print(preco) # valor: 10.0

preco = 10 / 2
print(preco) # valor: 5.0

# Numérico para string
preco = 19.90
print(str(preco)) # valor: '19.9'

Idade = 30
print(str(Idade)) # valor: '30'

texto = f"Idade: {Idade} preco: {preco}"
print(texto) # valor: 'Idade: 30 preco: 19.9'

# String para numérico
preco = "19.90"
print(float(preco)) # valor: 19.9

Idade = "30"
print(int(Idade)) # valor: 30

