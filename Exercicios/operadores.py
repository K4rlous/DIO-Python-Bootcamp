# Tipos de Operadores

# Operadores Aritméticos
soma = 5 + 3
subtracao = 10 - 4
multiplicacao = 6 * 7
divisao = 20 / 5
modulo = 10 % 3
exponenciacao = 2 ** 3
divisao_inteira = 15 // 4

# Operadores de Comparação
igual = (5 == 5)
diferente = (5 != 3)
maior_que = (10 > 5)
menor_que = (3 < 7)
maior_ou_igual = (8 >= 8)
menor_ou_igual = (4 <= 6)


# Operadores de Atribuição
x = 10
x += 5  # x = x + 5
x -= 3  # x = x - 3
x *= 2  # x = x * 2
x /= 4  # x = x / 4
x %= 3  # x = x % 3


# Operadores Lógicos
e = (True and False)
ou = (True or False)
nao = not True # Negação

# Operadores de Idêntidade
a = [1, 2, 3]
b = a
identico = (a is b)
nao_identico = (a is not [1, 2, 3])

# Operadores de Associação
frutas = ['maçã', 'banana', 'laranja']
tem_maca = ('maçã' in frutas)
nao_tem_uva = ('uva' not in frutas)